// Basic logging
console.log('Hello World');

// Writing multiple values (they are space-separated)
console.log('User:', 'Alice', 'ID:', 101);

// Formatting with specifiers (%s for string, %d for number)
console.log('My %s has %d ears', 'cat', 2);

// Writing without a newline
process.stdout.write('Loading... ');
process.stdout.write('Done!\n'); // Manual newline
